public class DataTypeDemo {
    /*
        数据类型

            1.基本数据类型

                整数
                        byte        1个字节
                        short       2个字节
                        int（默认）     4个字节（首选）
                        long        8个字节（int范围不够报错，将数据换成long，定义long类型数据需要加入L的标识）

                小数
                        float       4个字节（非要用float类型，需加入F标识）
                        double（默认）      8个字节（首选）

                字符
                        char        2个字节

                布尔
                        boolean     1个字节


            2.引用数据类型

                字符串
                        string
     */
    public static void main(String[] args) {
        long tel = 18827861475L;//定义long类型数据需要加入L的表识

        float height = 172.3F;//定义float类型数据需要加入F的表识

        char gender = '男';

    }
}
